<h1 class="text-warning">Единицы измерения</h1>

<p>Исторически так сложилось, что разработчики всегда использовали пиксели при создании компьютерных интерфейсов. Но с развитием технологий данный подход стал источником проблем - на многих новых дисплеях элементы стали очень мелкими при установленных высоких разрешениях. Поэтому стали появляться новые единицы измерения, не зависящие от разрешения экрана.</p>

<p>Android поддерживает несколько стандартных единиц измерения. Вкратце перечислим их.</p>
<ul>
<li>px (pixels) — пиксели. Точки на экране - минимальные единицы измерения;</li>
<li>dp (density-independent pixels) — независимые от плотности пиксели. Абстрактная единица измерения, основанная на физической плотности экрана с разрешением 160 dpi. В этом случае 1dp = 1px;</li>
<li>dip - синоним для dp. Иногда используется в примерах Google;</li>
<li>sp (scale-independent pixels) — независимые от масштабирования пиксели. Допускают настройку размеров, производимую пользователем. Полезны при работе с шрифтами;</li>
<li>in (inches) — дюймы, базируются на физических размерах экрана. Можно измерить обычной линейкой;</li>
<li>mm (millimeters) — миллиметры, базируются на физических размерах экрана. Можно измерить обычной линейкой;</li>
<li>pt (points) — 1/72 дюйма, базируются на физических размерах экрана;</li>
</ul>

<p>Как правило, при установке размера текста используются единицы измерения <b>sp</b>, которые наиболее корректно отображают шрифты:</p>
<pre><code class="xml">
android:textSize="48sp"
</code></pre>

<p>В остальных случаях рекомендуется использовать <b>dp</b>.</p>

<h2 class="text-warning" id="dp2px">Переводим dp в пиксели</h2>

<p>Так как на разных устройствах <b>dp</b> может различаться, то для получения величин в пикселях и наоборот используйте методы:</p>

<pre><code class="java">
private float dpFromPx(float px) {
	return px
			/ getApplicationContext().getResources().getDisplayMetrics().density;
}

private float pxFromDp(float dp) {
	return dp
			* getApplicationContext().getResources().getDisplayMetrics().density;
}
</code></pre>

<p>Если вы не определили размеры в XML, то их можно задать программно с помощью следующего кода (устанавливаем отступы для компонента):</p>

<pre><code class="java">
final float scale = getResources().getDisplayMetrics().density;
int padding_5dp = (int) (5 * scale + 0.5f);
int padding_20dp = (int) (20 * scale + 0.5f);
int padding_50dp = (int) (50 * scale + 0.5f);
 
RadioButton rb = new RadioButton(this);
rb.setText("My Radio Button");
rb.setLayoutParams(new LayoutParams(padding_50dp,padding_50dp));
rb.setPadding(padding_20dp,padding_5dp,padding_5dp,padding_5dp);
</code></pre>

<h2 class="text-warning">Настройка шрифтов</h2>

<p>Давайте чуть подробнее поговорим о работе со шрифтами, чтобы лучше понять специфику работы с текстами. Все люди разные - у кого-то зрение хорошое, у кого-то плохое. Android позволяет в настройках задать размеры шрифта в четырёх вариантах: Мелкий, Обычный, Крупный, Огромный. Для этого нужно зайти в Настройки | Экран | Размер шрифта.</p>

<p><img src="../../../netsources.narod.ru/images/fontsize.png" alt="Выбор размера шрифта"></p>

<p>Если вы хотите, чтобы ваш текст мог меняться в зависимости от выбора пользователя, то используйте единицы измерения SP:</p>

<pre><code class="xml">
&lt;TextView
    android:id=&quot;@+id/textView1&quot;
    android:layout_width=&quot;wrap_content&quot;
    android:layout_height=&quot;wrap_content&quot;
    android:text=&quot;Текст с размером 26sp&quot;
    android:textSize=&quot;26sp&quot; /&gt;
</code></pre>

<p>В тех случаях, когда изменять текст по желанию пользователя не следует, но при этом текст должен отображаться корректно в зависимости от разрешения экрана устройства, то используйте единицы измерения DP.</p>

<p>Третий вариант - если вы ни при каких обстоятельствах (какой же вы упрямый) не хотите зависеть от предпочтений пользователя и разрешения экрана, то пользуйтесь PX (пиксели). Среда разработки будет сопротивляться вашему желанию и выводить предупреждающие сообщения. Подумайте ещер раз о своём решении.</p>

<h2 class="text-warning">Используем стандартные системные размеры шрифтов</h2>

<p>В Android зашиты три системный размера шрифтов, основанных на SP: Small, Medium и Large. Вы можете использовать их в стандартных случаях, когда вам не нужно задавать конкретные значения (атрибут style):</p>

<pre><code class="xml">
&lt;TextView
    android:id=&quot;@+id/textView1&quot;
    style=&quot;@android:style/TextAppearance.Small&quot;
    android:layout_width=&quot;wrap_content&quot;
    android:layout_height=&quot;wrap_content&quot;
    android:text=&quot;Пример текста - Small&quot; /&gt;
&lt;TextView
    android:id=&quot;@+id/textView2&quot;
    style=&quot;@android:style/TextAppearance.Medium&quot;
    android:layout_width=&quot;wrap_content&quot;
    android:layout_height=&quot;wrap_content&quot;
    android:text=&quot;Пример текста  - Medium&quot; /&gt;
&lt;TextView
    android:id=&quot;@+id/textView3&quot;
    style=&quot;@android:style/TextAppearance.Large&quot;
    android:layout_width=&quot;wrap_content&quot;
    android:layout_height=&quot;wrap_content&quot;
    android:text=&quot;Пример текста  - Large&quot; /&gt;
</code></pre>

<p>На самом деле стилей <b>@android:style/TextAppearance.*</b> гораздо больше. Если вы вдруг забыли про названия стилей, то можете использовать встроенные возможности среды разработки. На панели инструментов виджет TextView представлен в четырёх вариантах: TextView, Large, Medium, Small, и в них используется атрибут <b>android:textAppearance</b>.</p>

<p>Создадим проект со всеми возможными вариантами и посмотрим на результат. В первом случае будем использовать стандартные настройки шрифта, во втором - увеличим его.</p>

<pre><code class="xml">
